-- Idempotent baseline for both fresh installations and databases previously
-- managed by Hibernate ddl-auto=update. Flyway is baselined at version 0.

CREATE TABLE IF NOT EXISTS users (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(255) NOT NULL,
    email VARCHAR(255) NOT NULL UNIQUE,
    password_hash VARCHAR(255) NOT NULL,
    role VARCHAR(40) NOT NULL,
    created_at TIMESTAMP NOT NULL
);

CREATE TABLE IF NOT EXISTS drivers (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(255) NOT NULL,
    phone VARCHAR(255) NOT NULL,
    license_number VARCHAR(255) NOT NULL UNIQUE,
    status VARCHAR(40) NOT NULL,
    current_vehicle VARCHAR(255),
    deliveries_completed INTEGER NOT NULL DEFAULT 0,
    success_rate NUMERIC(5,2) NOT NULL DEFAULT 100
);

CREATE TABLE IF NOT EXISTS vehicles (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    plate VARCHAR(12) NOT NULL UNIQUE,
    model VARCHAR(255) NOT NULL,
    capacity_kg NUMERIC(10,2) NOT NULL,
    status VARCHAR(40) NOT NULL,
    linked_driver_id BIGINT REFERENCES drivers(id)
);

CREATE TABLE IF NOT EXISTS route_plans (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(255) NOT NULL,
    origin VARCHAR(255) NOT NULL,
    destination VARCHAR(255) NOT NULL,
    estimated_distance_km NUMERIC(8,2) NOT NULL,
    estimated_time_minutes INTEGER NOT NULL,
    status VARCHAR(40) NOT NULL,
    origin_lat DOUBLE PRECISION NOT NULL,
    origin_lng DOUBLE PRECISION NOT NULL,
    destination_lat DOUBLE PRECISION NOT NULL,
    destination_lng DOUBLE PRECISION NOT NULL,
    color VARCHAR(16) NOT NULL
);

CREATE TABLE IF NOT EXISTS delivery_orders (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    order_number VARCHAR(32) NOT NULL UNIQUE,
    tracking_code VARCHAR(40) NOT NULL UNIQUE,
    customer_name VARCHAR(255) NOT NULL,
    phone VARCHAR(255) NOT NULL,
    address VARCHAR(255) NOT NULL,
    city VARCHAR(255) NOT NULL,
    state VARCHAR(2) NOT NULL,
    order_value NUMERIC(12,2) NOT NULL,
    weight_kg NUMERIC(10,2) NOT NULL DEFAULT 0,
    status VARCHAR(40) NOT NULL,
    created_at TIMESTAMP NOT NULL,
    expected_delivery_at TIMESTAMP NOT NULL
);

ALTER TABLE delivery_orders ADD COLUMN IF NOT EXISTS tracking_code VARCHAR(40);
