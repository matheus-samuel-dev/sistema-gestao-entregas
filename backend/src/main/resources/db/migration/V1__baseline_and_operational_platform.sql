WHERE tracking_code IS NULL OR tracking_code = '';
UPDATE delivery_orders SET weight_kg = 0 WHERE weight_kg IS NULL;
ALTER TABLE delivery_orders ALTER COLUMN tracking_code SET NOT NULL;
ALTER TABLE delivery_orders ALTER COLUMN weight_kg SET NOT NULL;
CREATE UNIQUE INDEX IF NOT EXISTS uk_orders_tracking_code ON delivery_orders(tracking_code);

CREATE TABLE IF NOT EXISTS deliveries (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    version BIGINT NOT NULL DEFAULT 0,
    order_id BIGINT NOT NULL REFERENCES delivery_orders(id),
    driver_id BIGINT NOT NULL REFERENCES drivers(id),
    vehicle_id BIGINT NOT NULL REFERENCES vehicles(id),
    route_id BIGINT REFERENCES route_plans(id),
    origin VARCHAR(255) NOT NULL,
    destination VARCHAR(255) NOT NULL,
    expected_at TIMESTAMP NOT NULL,
    delivered_at TIMESTAMP,
    status VARCHAR(40) NOT NULL,
    progress INTEGER NOT NULL,
    current_lat DOUBLE PRECISION NOT NULL,
    current_lng DOUBLE PRECISION NOT NULL,
    created_at TIMESTAMP NOT NULL
);

ALTER TABLE deliveries ADD COLUMN IF NOT EXISTS version BIGINT DEFAULT 0;
UPDATE deliveries SET version = 0 WHERE version IS NULL;
ALTER TABLE deliveries ALTER COLUMN version SET NOT NULL;

CREATE TABLE IF NOT EXISTS delivery_timelines (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    delivery_id BIGINT NOT NULL REFERENCES deliveries(id) ON DELETE CASCADE,
    title VARCHAR(255) NOT NULL,
    description VARCHAR(255) NOT NULL,
    timestamp TIMESTAMP NOT NULL,
    status VARCHAR(40) NOT NULL
);

CREATE TABLE IF NOT EXISTS incidents (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    delivery_id BIGINT REFERENCES deliveries(id),
    order_id BIGINT REFERENCES delivery_orders(id),
    type VARCHAR(60) NOT NULL,
    priority VARCHAR(30) NOT NULL,
    status VARCHAR(40) NOT NULL,
    responsible VARCHAR(255) NOT NULL,
    description VARCHAR(1000) NOT NULL,
    resolution VARCHAR(1000),
    created_at TIMESTAMP NOT NULL,
    updated_at TIMESTAMP NOT NULL
);

